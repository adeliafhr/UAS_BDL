    private void cbIDR(){
       try {
            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);
            stmt = conn.createStatement();

            String sqlPelanggan = "SELECT * FROM detail_pembelian";
            ResultSet rsPelanggan = stmt.executeQuery(sqlPelanggan);

            cbIDR.removeAllItems(); // Membersihkan item sebelumnya jika ada

            while (rsPelanggan.next()) {
                cbIDR.addItem(rsPelanggan.getString("id_restock"));
            }

            rsPelanggan.close();
            stmt.close();
            conn.close();

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memuat data restock: " + ex.getMessage());
        }
    }
    
