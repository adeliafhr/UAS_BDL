    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        String id_restock, id_produk, jumlah_produk_pembelian, subtotal_pembelian;
        if (cbIDR.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (cbIDP.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfJumlah.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfSub.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            try {
                Class.forName(driver);
                conn = DriverManager.getConnection(koneksi, user, password);
                conn.setAutoCommit(false);

                String sql = "INSERT INTO detail_pembelian VALUES(?,?,?,?)";
                pstmt = conn.prepareStatement(sql);

                id_restock = (String) cbIDR.getSelectedItem();
                id_produk = (String) cbIDP.getSelectedItem();   
                jumlah_produk_pembelian = tfJumlah.getText();
                subtotal_pembelian = tfSub.getText();

                pstmt.setString(1, id_restock);
                pstmt.setString(2, id_produk);
                pstmt.setInt(3, Integer.parseInt(jumlah_produk_pembelian));
                pstmt.setInt(5, Integer.parseInt(subtotal_pembelian));

                pstmt.executeUpdate();
                conn.commit();
                pstmt.close();
                conn.close();

                JOptionPane.showMessageDialog(null, "Data berhasil ditambah");
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat pengisian");
            }
        }
        tampil(); 
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String id_restock, id_produk, jumlah_produk_pembelian, subtotal_pembelian;
        if (cbIDR.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (cbIDP.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfJumlah.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfSub.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            try {
                Class.forName(driver);
                String sql = "UPDATE detail_pembelian SET id_produk = ?, jumlah_produk_pembelian = ?, subtotal_produk = ? WHERE id_restock = ?";
                        
                conn = DriverManager.getConnection(koneksi, user, password);
                pstmt = conn.prepareStatement(sql);

                id_restock = (String) cbIDR.getSelectedItem();
                id_produk = (String) cbIDP.getSelectedItem();   
                jumlah_produk_pembelian = tfJumlah.getText();
                subtotal_pembelian = tfSub.getText();

                pstmt.setString(1, id_produk);
                pstmt.setInt(2, Integer.parseInt(jumlah_produk_pembelian));
                pstmt.setInt(3, Integer.parseInt(subtotal_pembelian));
                pstmt.setString(4, id_restock);

                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
                    pstmt.close();
                    conn.close();
                 
                } else {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(detail_pembelian.class.getName()).log(Level.SEVERE, null, ex);

            }
        }
        tampil();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String id_restock = (String) cbIDR.getSelectedItem();
        String id_produk = (String) cbIDP.getSelectedItem();

        if (id_restock != null && id_produk != null) {
            try {
                Class.forName(driver);

                try ( Connection conn = DriverManager.getConnection(koneksi, user, password);  PreparedStatement pstmt = conn.prepareStatement("DELETE FROM detail_penjualan WHERE id_penjualan = ? and id_jajan = ?")) {

                    pstmt.setString(1, id_restock);
                    pstmt.setString(2, id_produk);
                    int affectedRows = pstmt.executeUpdate();

                    if (affectedRows > 0) {
                        JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    } else {
                        JOptionPane.showMessageDialog(null, "Tidak ada data yang ditemukan untuk dihapus");
                    }
                }
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada koneksi database: " + ex.getMessage());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada SQL query: " + ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "ID restock atau ID produk tidak boleh kosong");
        }
        tampil();
    }//GEN-LAST:event_btnDeleteActionPerformed

